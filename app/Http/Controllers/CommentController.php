     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Post $post)
    {

        //Validate fields
        $request->validate([
            'name' => 'required',
            'comment' => 'required'
        ]);


        //Store Comment
        $post->comment()->create([
            'name' => $request->name,
            'comment' => $request->comment
        ]);

        //Redirect Back
        return back();

       


    }

